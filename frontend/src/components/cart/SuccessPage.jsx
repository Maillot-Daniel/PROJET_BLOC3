import React, { useState, useEffect, useCallback } from "react";
import { useSearchParams } from "react-router-dom";
import QRCode from "qrcode";
import html2canvas from "html2canvas";
import jsPDF from "jspdf";

function SuccessPage() {
  const [searchParams] = useSearchParams();
  const sessionId = searchParams.get("session_id");
  const [billets, setBillets] = useState([]);
  const [chargement, setChargement] = useState(true);
  const [statut, setStatut] = useState("");
  const [totalStripe, setTotalStripe] = useState("0.00");
  // SUPPRIM√â: 'donneesSessionStripe' n'est pas utilis√©

  const CLE_STOCKAGE = "oly_billets";
  const URL_API = "https://projet-bloc3.onrender.com";

  // Debug am√©lior√©
  const debugStockageLocal = useCallback(() => {
    console.log("üîç DEBUG Stockage Local:");
    console.log("üõí panier_olympiques:", localStorage.getItem("panier_olympiques"));
    console.log("üé´ oly_billets:", localStorage.getItem(CLE_STOCKAGE));
    console.log("üîó sessionId:", sessionId);
    
    try {
      const panier = JSON.parse(localStorage.getItem("panier_olympiques") || "[]");
      console.log("üìä CONTENU DU PANIER:", panier);
      console.log("üî¢ Nombre d'articles dans le panier:", panier.length);
    } catch (erreur) {
      console.error("‚ùå Erreur analyse panier:", erreur);
    }
  }, [sessionId, CLE_STOCKAGE]);

  // R√©cup√©ration session Stripe - AVEC LE BON ENDPOINT
  const recupererSessionStripe = useCallback(async (sessionId) => {
    if (!sessionId) {
      console.log("‚ùå Aucun sessionId fourni pour Stripe");
      return null;
    }
    
    console.log("üîÑ R√©cup√©ration session Stripe avec ID:", sessionId);
    try {
      // CORRECTION: Utiliser le bon endpoint api/pay/session/
      console.log("üì° Appel API vers:", `${URL_API}/api/pay/session/${sessionId}`);
      
      const reponse = await fetch(`${URL_API}/api/pay/session/${sessionId}`);
      console.log("üì° R√©ponse Stripe status:", reponse.status);
      
      if (reponse.ok) {
        const donneesSession = await reponse.json();
        console.log("‚úÖ Donn√©es session Stripe COMPL√àTES:", donneesSession);
        
        // Extraction du montant
        let totalReel = "0.00";
        if (donneesSession.amount_total) {
          totalReel = (donneesSession.amount_total / 100).toFixed(2);
          console.log("üí∞ Montant total (amount_total):", donneesSession.amount_total, "‚Üí", totalReel + "‚Ç¨");
        } else if (donneesSession.amount) {
          totalReel = (donneesSession.amount / 100).toFixed(2);
          console.log("üí∞ Montant (amount):", donneesSession.amount, "‚Üí", totalReel + "‚Ç¨");
        }
        
        setTotalStripe(totalReel);
        return donneesSession;
      } else {
        console.error("‚ùå Erreur r√©ponse Stripe:", reponse.status, reponse.statusText);
        const texteErreur = await reponse.text();
        console.error("‚ùå D√©tails erreur:", texteErreur);
      }
    } catch (erreur) {
      console.error("‚ùå Erreur r√©cup√©ration session Stripe:", erreur);
    }
    return null;
  }, [URL_API]);

  const genererQRCodePourEvenement = useCallback(async (numeroCommande, evenement) => {
    try {
      const contenuQR = {
        idCommande: numeroCommande,
        titreEvenement: evenement.eventTitle || "√âv√©nement Olympique",
        prix: evenement.prix || evenement.price || 0,
        quantite: evenement.quantite || evenement.quantity || 1,
        horodatage: Date.now(),
      };
      
      const imageQRCode = await QRCode.toDataURL(JSON.stringify(contenuQR), {
        width: 200,
        margin: 2,
        color: { dark: "#0055A4", light: "#FFFFFF" },
      });
      
      return imageQRCode;
    } catch (erreur) {
      console.error("‚ùå Erreur g√©n√©ration QR Code:", erreur);
      return null;
    }
  }, []);

  // Cr√©er des billets depuis les donn√©es Stripe - CORRIG√â avec la d√©pendance manquante
  const creerBilletsDepuisStripe = useCallback(async (sessionStripe, numeroCommande) => {
    console.log("üé´ Cr√©ation billets depuis donn√©es Stripe:", sessionStripe);
    
    if (!sessionStripe) {
      console.log("‚ùå Aucune donn√©e Stripe disponible");
      return [];
    }

    const billetsGeneres = [];
    const dateAchatISO = new Date().toISOString();

    // Si on a des line_items dans Stripe, on les utilise
    if (sessionStripe.line_items && sessionStripe.line_items.data) {
      console.log("üõí Articles trouv√©s dans Stripe:", sessionStripe.line_items.data.length);
      
      let billetIndex = 0;
      for (const item of sessionStripe.line_items.data) {
        billetIndex++;
        const description = item.description || "√âv√©nement Olympique";
        const quantite = item.quantity || 1;
        const prixUnitaire = item.price?.unit_amount ? item.price.unit_amount / 100 : 1683.00; // Utiliser le montant r√©el
        
        console.log(`üì¶ Traitement article Stripe ${billetIndex}:`, {
          description,
          quantite,
          prixUnitaire,
          total: (prixUnitaire * quantite).toFixed(2)
        });

        // G√©n√©rer QR Code
        const qrCode = await genererQRCodePourEvenement(numeroCommande, {
          eventTitle: description,
          prix: prixUnitaire,
          quantite: quantite
        });

        const billet = {
          id: `${numeroCommande}-STRIPE-${billetIndex}`,
          numeroCommande,
          idEvenement: billetIndex,
          titreEvenement: description,
          dateEvenement: "2024", // Par d√©faut
          lieuEvenement: "Paris", // Par d√©faut
          typeOffre: "Standard",
          quantite: quantite,
          prix: prixUnitaire,
          total: (prixUnitaire * quantite).toFixed(2),
          qrCode,
          dateAchat: dateAchatISO,
          statut: "actif",
          source: "stripe"
        };
        
        billetsGeneres.push(billet);
        console.log(`‚úÖ Billet Stripe cr√©√©: ${billet.titreEvenement}`);
      }
    } else {
      // Fallback: cr√©er un billet bas√© sur le montant total
      console.log("‚ö†Ô∏è Aucun line_item trouv√©, cr√©ation billet bas√© sur montant total");
      const montantTotal = parseFloat(totalStripe) || 1683.00;
      const qrCode = await genererQRCodePourEvenement(numeroCommande, {
        eventTitle: "√âv√©nement Olympique",
        prix: montantTotal,
        quantite: 1
      });

      const billet = {
        id: `${numeroCommande}-FALLBACK`,
        numeroCommande,
        titreEvenement: "√âv√©nement Olympique",
        dateEvenement: "2024",
        lieuEvenement: "Paris",
        typeOffre: "Standard",
        quantite: 1,
        prix: montantTotal,
        total: montantTotal.toFixed(2),
        qrCode,
        dateAchat: dateAchatISO,
        statut: "actif",
        source: "fallback"
      };
      
      billetsGeneres.push(billet);
      console.log(`‚úÖ Billet fallback cr√©√©: ${montantTotal}‚Ç¨`);
    }

    return billetsGeneres;
  }, [totalStripe, genererQRCodePourEvenement]); // CORRIG√â: d√©pendance ajout√©e

  const sauvegarderBilletsStockage = useCallback((nouveauxBillets) => {
    try {
      localStorage.setItem(CLE_STOCKAGE, JSON.stringify(nouveauxBillets));
      console.log("‚úÖ Billets sauvegard√©s:", nouveauxBillets.length);
    } catch (erreur) {
      console.error("‚ùå Erreur sauvegarde billets:", erreur);
    }
  }, [CLE_STOCKAGE]);

  // G√âN√âRATION PRINCIPALE DES BILLETS - VERSION CORRIG√âE
  const genererBillets = useCallback(async () => {
    console.log("üöÄ D√âBUT - G√©n√©ration des billets");
    setStatut("Cr√©ation de vos billets...");
    
    debugStockageLocal();
    
    try {
      const panier = JSON.parse(localStorage.getItem("panier_olympiques") || "[]");
      const numeroCommande = "OLY-" + Date.now();
      let billetsGeneres = [];

      console.log("üì¶ Panier trouv√©:", panier.length, "articles");
      console.log("üí∞ Montant Stripe connu:", totalStripe);

      // PRIORIT√â 1: R√©cup√©rer les donn√©es Stripe
      if (sessionId) {
        console.log("üîó Session ID d√©tect√©, r√©cup√©ration donn√©es Stripe...");
        const sessionStripe = await recupererSessionStripe(sessionId);
        
        if (sessionStripe) {
          console.log("‚úÖ Donn√©es Stripe r√©cup√©r√©es, cr√©ation billets...");
          billetsGeneres = await creerBilletsDepuisStripe(sessionStripe, numeroCommande);
        } else {
          console.log("‚ùå √âchec r√©cup√©ration Stripe, utilisation du panier local");
        }
      }

      // PRIORIT√â 2: Utiliser le panier local si Stripe √©choue ou n'a pas de donn√©es
      if (billetsGeneres.length === 0 && panier.length > 0) {
        console.log("üõí Utilisation du panier local pour cr√©er les billets");
        let totalCalcul√© = 0;
        
        for (const article of panier) {
          const qrCode = await genererQRCodePourEvenement(numeroCommande, article);
          const prixUnitaire = article.prix || article.price || 0;
          const quantite = article.quantite || article.quantity || 1;
          const totalArticle = prixUnitaire * quantite;
          totalCalcul√© += totalArticle;
          
          const billet = {
            id: `${numeroCommande}-${article.eventId || article.id || Date.now()}`,
            numeroCommande,
            titreEvenement: article.eventTitle || article.nom || "√âv√©nement Olympique",
            dateEvenement: article.eventDate || article.date || "2024",
            lieuEvenement: article.eventLocation || article.lieu || "Paris",
            typeOffre: article.offerType || article.type || "Standard",
            quantite: quantite,
            prix: prixUnitaire,
            total: totalArticle.toFixed(2),
            qrCode,
            dateAchat: new Date().toISOString(),
            statut: "actif",
            source: "panier_local"
          };
          billetsGeneres.push(billet);
        }
        
        // Mettre √† jour le total si pas d√©j√† fait par Stripe
        if (totalStripe === "0.00") {
          setTotalStripe(totalCalcul√©.toFixed(2));
        }
      }

      // PRIORIT√â 3: Fallback - cr√©er au moins un billet avec le montant pay√©
      if (billetsGeneres.length === 0) {
        console.log("üÜò Aucun billet cr√©√©, cr√©ation d'un billet de secours");
        const montant = parseFloat(totalStripe) > 0 ? totalStripe : "1683.00";
        const qrCode = await genererQRCodePourEvenement(numeroCommande, {
          eventTitle: "√âv√©nement Olympique",
          prix: parseFloat(montant),
          quantite: 1
        });

        billetsGeneres.push({
          id: `${numeroCommande}-SECOURS`,
          numeroCommande,
          titreEvenement: "√âv√©nement Olympique",
          dateEvenement: "2024",
          lieuEvenement: "Paris",
          typeOffre: "Standard",
          quantite: 1,
          prix: parseFloat(montant),
          total: montant,
          qrCode,
          dateAchat: new Date().toISOString(),
          statut: "actif",
          source: "secours"
        });
      }

      console.log("üéâ Billets g√©n√©r√©s:", billetsGeneres.length);
      console.log("üí∞ Total final:", totalStripe);

      setBillets(billetsGeneres);
      sauvegarderBilletsStockage(billetsGeneres);
      
      // Nettoyer le panier seulement si on a r√©ussi √† cr√©er des billets
      if (billetsGeneres.length > 0) {
        localStorage.removeItem("panier_olympiques");
        console.log("üóëÔ∏è Panier nettoy√©");
      }
      
      setChargement(false);
      setStatut(`‚úÖ ${billetsGeneres.length} billet(s) cr√©√©(s) avec succ√®s !`);
      
    } catch (erreur) {
      console.error("‚ùå Erreur cr√©ation billets:", erreur);
      setStatut("Erreur lors de la cr√©ation des billets");
      setChargement(false);
    }
  }, [
    sessionId, 
    totalStripe, 
    recupererSessionStripe, 
    creerBilletsDepuisStripe, 
    genererQRCodePourEvenement, 
    sauvegarderBilletsStockage, 
    debugStockageLocal
  ]);

  // Reste du code (t√©l√©chargement PDF, impression, etc.)
  const telechargerBilletPDF = async (billet) => {
    const elementBillet = document.getElementById(`billet-${billet.id}`);
    if (!elementBillet) return;
    
    try {
      setStatut(`G√©n√©ration PDF...`);
      const canvas = await html2canvas(elementBillet, { scale: 2 });
      const imgData = canvas.toDataURL("image/png");
      const pdf = new jsPDF();
      const pdfWidth = pdf.internal.pageSize.getWidth();
      const pdfHeight = (canvas.height * pdfWidth) / canvas.width;
      
      pdf.addImage(imgData, "PNG", 0, 0, pdfWidth, pdfHeight);
      pdf.save(`billet-${billet.numeroCommande}.pdf`);
      setStatut("PDF t√©l√©charg√© !");
    } catch (erreur) {
      console.error("‚ùå Erreur g√©n√©ration PDF:", erreur);
      setStatut("Erreur lors du t√©l√©chargement");
    }
  };

  const imprimerBillet = (billet) => {
    const elementBillet = document.getElementById(`billet-${billet.id}`);
    if (!elementBillet) return;
    
    const printWindow = window.open("", "_blank");
    printWindow.document.write(`
      <html>
        <head><title>Billet ${billet.titreEvenement}</title></head>
        <body>${elementBillet.outerHTML}</body>
      </html>
    `);
    printWindow.document.close();
    printWindow.print();
  };

  useEffect(() => {
    console.log("üéØ SuccessPage mont√© - D√©but g√©n√©ration billets");
    genererBillets();
  }, [genererBillets]);

  if (chargement) {
    return (
      <div style={{ textAlign: "center", padding: 50 }}>
        <h2>{statut}</h2>
        <div style={{ margin: 20 }}>
          <div style={{
            width: 50, height: 50,
            border: "5px solid #f3f3f3",
            borderTop: "5px solid #0055A4",
            borderRadius: "50%",
            animation: "spin 1s linear infinite",
            margin: "0 auto",
          }}></div>
        </div>
        <p>Pr√©paration de vos billets...</p>
        <style>{`@keyframes spin { 0% { transform: rotate(0deg); } 100% { transform: rotate(360deg); } }`}</style>
      </div>
    );
  }

  return (
    <div style={{ textAlign: "center", padding: 30, maxWidth: 800, margin: "0 auto" }}>
      <div style={{ background: "linear-gradient(135deg, #0055A4 0%, #EF4135 100%)", color: "white", padding: 30, borderRadius: 15, marginBottom: 30 }}>
        <h1 style={{ margin: 0, fontSize: "2.5em" }}>üéâ Paiement R√©ussi !</h1>
        <p style={{ fontSize: "1.2em", marginTop: 10 }}>
          Vous avez {billets.length} billet{billets.length > 1 ? "s" : ""}
        </p>
        <p style={{ fontSize: "1.1em", marginTop: 5 }}>
          <strong>Total pay√©: {totalStripe} ‚Ç¨</strong>
        </p>
      </div>

      {billets.map((billet) => (
        <div key={billet.id} style={{ marginBottom: 30 }}>
          <div id={`billet-${billet.id}`} style={{ border: "3px solid #0055A4", padding: 25, background: "white", borderRadius: 12, boxShadow: "0 8px 25px rgba(0,0,0,0.1)" }}>
            <h2 style={{ color: "#0055A4", margin: "0 0 15px 0" }}>üé´ {billet.titreEvenement}</h2>
            <p><strong>üìç Lieu:</strong> {billet.lieuEvenement}</p>
            <p><strong>üìÖ Date:</strong> {billet.dateEvenement}</p>
            <p><strong>üéØ Type:</strong> {billet.typeOffre}</p>
            <p><strong>üéüÔ∏è Quantit√©:</strong> {billet.quantite}</p>
            <p><strong>üí∞ Total:</strong> {billet.total} ‚Ç¨</p>
            <p><strong>üìã Commande:</strong> {billet.numeroCommande}</p>
            {billet.qrCode && <img src={billet.qrCode} alt="QR Code" style={{ width: 150, height: 150, margin: "15px 0" }} />}
          </div>
          
          <div style={{ display: "flex", gap: 10, justifyContent: "center", marginTop: 15 }}>
            <button onClick={() => telechargerBilletPDF(billet)} style={{ padding: "8px 15px", backgroundColor: "#28a745", color: "white", border: "none", borderRadius: 4, cursor: "pointer" }}>
              üì• PDF
            </button>
            <button onClick={() => imprimerBillet(billet)} style={{ padding: "8px 15px", backgroundColor: "#007bff", color: "white", border: "none", borderRadius: 4, cursor: "pointer" }}>
              üñ®Ô∏è Imprimer
            </button>
          </div>
        </div>
      ))}

      {statut && <p style={{ color: "#0055A4", fontStyle: "italic", marginTop: 15 }}>{statut}</p>}
    </div>
  );
}

export default SuccessPage;